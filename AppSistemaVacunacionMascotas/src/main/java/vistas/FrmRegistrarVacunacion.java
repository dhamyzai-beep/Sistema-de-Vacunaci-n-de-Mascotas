    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    String nombreMascota = txtNombreMascota.getText().trim();

    if (nombreMascota.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el nombre de la mascota");
        return;
    }

    boolean encontrado = false;

    for (Vacunacion v : DatosApp.vacunaciones) {
    if (v.getMascota().getNombre().equalsIgnoreCase(nombreMascota)) {

        v.setVacuna(txtNombreVacuna.getText());
        v.setDosis(jComboDosis.getSelectedItem().toString());
        v.setFecha(txtFechaVacuna.getText());
        v.setObservaciones(txtObservaciones.getText());

        encontrado = true;
        break;
    }
}

    if (encontrado) {
        JOptionPane.showMessageDialog(this, "Vacunaci칩n actualizada correctamente");
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "No existe una vacunaci칩n registrada para esa mascota");
    }
}  
    private void limpiarCampos() {
    txtNombreMascota.setText("");
    txtNombreVacuna.setText("");
    jComboDosis.setSelectedIndex(0);
    txtFechaVacuna.setText("");
    txtObservaciones.setText("");
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
 String nombreMascota = txtNombreMascota.getText().trim();

    Mascota mascota = buscarMascotaPorNombre(nombreMascota);

    if (mascota == null) {
        JOptionPane.showMessageDialog(this, "La mascota no est치 registrada");
        return;
    }

    Vacunacion v = new Vacunacion(
            mascota,
            txtNombreVacuna.getText(),
            jComboDosis.getSelectedItem().toString(),
            txtFechaVacuna.getText(),
            txtObservaciones.getText()
    );

    DatosApp.vacunaciones.add(v);

    JOptionPane.showMessageDialog(this, "Vacunaci칩n guardada correctamente");
    limpiarCampos();
    
    System.out.println("Vacunaciones guardadas: " + DatosApp.vacunaciones.size());

        // TODO add your handling code here:  
    }//GEN-LAST:event_btnGuardarActionPerformed

